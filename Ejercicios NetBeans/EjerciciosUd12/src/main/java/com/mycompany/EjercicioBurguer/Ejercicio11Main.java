    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        double precio=8.0;
        if (jComboBoxHamburguesas.getSelectedItem().equals("Ternera") ||jComboBoxHamburguesas.getSelectedItem().equals("Vegana")) precio+=1;
        if (jComboBoxPatatas.getSelectedItem().equals("Caseras")) precio+=1;
        if (jRadioButtonDoble.isSelected()) precio+=2;
        if (jCheckBoxQueso.isSelected()) precio+=0.5;
        if (jCheckBoxPatatasExtra.isSelected()) precio+=1;

        precio=precio+(0.5*(int)jSpinnerSalsasKetchup.getValue())+(0.5*(int)jSpinnerSalsasMostaza.getValue())+(0.5*(int)jSpinnerSalsasBarbacoa.getValue())+(0.5*(int)jSpinnerSalsasThai.getValue());
        precio=precio*1.21;
        if (jRadioButtonRecogidaLocal.isSelected()) precio=(precio-(precio*0.2));
        jTextFieldImporte.setText(String.valueOf(precio));

    }//GEN-LAST:event_jButton1ActionPerformed

