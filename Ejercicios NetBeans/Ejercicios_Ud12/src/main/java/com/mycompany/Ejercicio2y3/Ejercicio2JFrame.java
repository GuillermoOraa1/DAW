    private void divisionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_divisionActionPerformed
        // TODO add your handling code here:
        double resultadoDivision=Integer.valueOf(valor1.getText())/Integer.valueOf(valor2.getText());
        resultado.setText(Double.toString(resultadoDivision));
        resetear();
    }//GEN-LAST:event_divisionActionPerformed

    private void calculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculoActionPerformed
        // TODO add your handling code here:
        if (sumatorio.isSelected()) {
            int resultadoSuma=Integer.valueOf(valor1.getText())+Integer.valueOf(valor2.getText());
            resultado.setText(Integer.toString(resultadoSuma));
        }
        if(productorio.isSelected()){
            int resultadoMultiplicacion=Integer.valueOf(valor1.getText())*Integer.valueOf(valor2.getText());
            resultado.setText(Integer.toString(resultadoMultiplicacion));
        }
        if (exponencial.isSelected()) {
            double resultadoPotencia=Math.pow(Double.valueOf(valor1.getText()), Double.valueOf(valor2.getText()));
            resultado.setText(Double.toString(resultadoPotencia));            
        }    
        resetear();
    }//GEN-LAST:event_calculoActionPerformed

    private void resetear(){
        valor1.setText("0");
        valor2.setText("0");
    }
